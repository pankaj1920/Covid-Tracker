package com.bohra.covid19tracker.data.SearchNews;

public class SearchNewsSources {
    private String id;
    private String name;

    public SearchNewsSources(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
